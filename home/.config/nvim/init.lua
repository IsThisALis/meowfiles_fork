vim.keymap.set("v", "K", ":m '<-2<CR>gv=gv", { desc = "Move selected lines up" })

vim.keymap.set("n", "W", function()
  local word = vim.fn.expand("<cword>")
  if word == "" then
    return
  end
  vim.cmd("normal! m'")
  vim.cmd("keepjumps normal! gg")
  vim.fn.search("\\<" .. word .. "\\>")
end, { silent = true })

